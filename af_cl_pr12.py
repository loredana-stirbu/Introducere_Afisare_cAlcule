print("Într-o gospodărie sunt 4 găini. Introduceţi în calculator prin variabilele a, b, c, d numărul de ouă pe care-l dă fiecare găină într-o zi. Afişaţi câte ouă se obţin într-o săptămână.")
a = int(input("Numarul de oua facute de prima gaina intr-o zi: "))
b = int(input("Numarul de oua facute de a doua gaina intr-o zi: "))
c = int(input("Numarul de oua facute de a treia gaina intr-o zi: "))
d = int(input("Numarul de oua facute de a patra gaina intr-o zi: "))
s=(a+b+c+d)*7
print("Intr-o saptamana se obtin",s,"oua")